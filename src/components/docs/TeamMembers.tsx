const ICONS: Record<string, IconCmp> = {
  github: GitHub,
  wordpress: WordPress,
  x: XIcon,
  twitter: XIcon,
  reddit: Reddit,
  bilibili: Bilibili,
  qq: QQ,
  blog: Globe,
}

export default function TeamMembers({ members }: { members: Member[] }) {
  if (!members?.length) return null
  return (
    <div className="not-prose my-6 grid gap-4 sm:grid-cols-2">
      {members.map((m, i) => (
        <div
          key={i}
          className="flex flex-col items-center rounded-2xl border bg-card p-6 text-center transition-shadow hover:shadow-sm"
        >
          {m.avatar && (
            <img
              src={m.avatar}
              alt={m.name}
              className="size-20 rounded-full border object-cover"
            />
          )}
          <h3 className="mt-4 font-semibold text-base">{m.name}</h3>
          {m.title && <p className="mt-0.5 text-primary text-sm">{m.title}</p>}
          {m.description && (
            <p className="mt-2 text-muted-foreground text-sm leading-relaxed">
              {m.description}
            </p>
          )}
          {m.links?.length ? (
            <div className="mt-3 flex items-center gap-1">
              {m.links.map((l, j) => {
                const Icon = ICONS[l.icon] ?? Globe
                return (
                  <a
                    key={j}
                    href={l.link}
                    target="_blank"
                    rel="noreferrer"
                    aria-label={l.icon}
                    className="inline-flex size-8 items-center justify-center rounded-md text-muted-foreground transition-colors hover:bg-accent hover:text-foreground"
                  >
                    <Icon className="size-[18px]" />
                  </a>
                )
              })}
            </div>
          ) : null}
        </div>
      ))}
    </div>
  )
}
